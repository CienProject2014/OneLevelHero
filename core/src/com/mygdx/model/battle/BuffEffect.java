package com.mygdx.model.battle;

public class BuffEffect {
	private int blockActionDuration;

	private int increaseAttack;
	private int decreaseAttack;
	private int increaseMagicAttack;
	private int decreaseMagicAttack;
	private int decreaseHp;
	private int decreaseHpDuration;
	private int decreaseHpMagicAttack;
	private int decreaseHpAttack;
	private int decreaseHpOffset;

	public int getBlockActionDuration() {
		return blockActionDuration;
	}

	public void setBlockActionDuration(int blockActionDuration) {
		this.blockActionDuration = blockActionDuration;
	}

	public int getIncreaseAttack() {
		return increaseAttack;
	}

	public void setIncreaseAttack(int increaseAttack) {
		this.increaseAttack = increaseAttack;
	}

	public int getDecreaseAttack() {
		return decreaseAttack;
	}

	public void setDecreaseAttack(int decreaseAttack) {
		this.decreaseAttack = decreaseAttack;
	}

	public int getIncreaseMagicAttack() {
		return increaseMagicAttack;
	}

	public void setIncreaseMagicAttack(int increaseMagicAttack) {
		this.increaseMagicAttack = increaseMagicAttack;
	}

	public int getDecreaseMagicAttack() {
		return decreaseMagicAttack;
	}

	public void setDecreaseMagicAttack(int decreaseMagicAttack) {
		this.decreaseMagicAttack = decreaseMagicAttack;
	}

	public int getDecreaseHp() {
		return decreaseHp;
	}

	public void setDecreaseHp(int decreaseHp) {
		this.decreaseHp = decreaseHp;
	}

	public int getDecreaseHpDuration() {
		return decreaseHpDuration;
	}

	public void setDecreaseHpDuration(int decreaseHpDuration) {
		this.decreaseHpDuration = decreaseHpDuration;
	}

	public int getDecreaseHpMagicAttack() {
		return decreaseHpMagicAttack;
	}

	public void setDecreaseHpMagicAttack(int decreaseHpMagicAttack) {
		this.decreaseHpMagicAttack = decreaseHpMagicAttack;
	}

	public int getDecreaseHpAttack() {
		return decreaseHpAttack;
	}

	public void setDecreaseHpAttack(int decreaseHpAttack) {
		this.decreaseHpAttack = decreaseHpAttack;
	}

	public int getDecreaseHpOffset() {
		return decreaseHpOffset;
	}

	public void setDecreaseHpOffset(int decreaseHpOffset) {
		this.decreaseHpOffset = decreaseHpOffset;
	}

}
