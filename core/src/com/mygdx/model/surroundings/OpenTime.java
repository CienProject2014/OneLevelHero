package com.mygdx.model.surroundings;

public class OpenTime {
	private int openTimeStart;
	private int openTimeEnd;
	public int getOpenTimeStart() {
		return openTimeStart;
	}
	public void setOpenTimeStart(int openTimeStart) {
		this.openTimeStart = openTimeStart;
	}
	public int getOpenTimeEnd() {
		return openTimeEnd;
	}
	public void setOpenTimeEnd(int openTimeEnd) {
		this.openTimeEnd = openTimeEnd;
	}
}
